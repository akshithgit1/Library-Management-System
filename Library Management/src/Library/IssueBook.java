    public class BookItem
    {
    
        int id;
        String name;
        public BookItem(int id,String name)
        {
        
            this.id=id;
            this.name=name;
            
        }
        public String toString()
        {
            return name;
        }
        
    }
    
    public void book()
    {
    
        try {
            
            pst=con.prepareStatement("select * from book");
            rs=pst.executeQuery();
            bc.removeAllItems();
            
            while(rs.next())
            {
                bc.addItem(new BookItem(rs.getInt(1),rs.getString(2)));
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
              
    }
    
    
    public void IssueBook_Load()
    {
    
        int c;
        try {
            pst=con.prepareStatement("select i.id,i.sid,s.studname,b.bname,i.idate,i.rdate from issuebook i JOIN student s ON i.sid=s.id JOIN book b ON i.bid=b.id");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
            
                Vector v2=new Vector();
                for(int i=1;i<=c;i++)
                {
                    v2.add(rs.getString("i.id"));
                    v2.add(rs.getString("i.sid"));
                    v2.add(rs.getString("s.studname"));
                    v2.add(rs.getString("b.bname"));
                    v2.add(rs.getString("i.idate"));
                    v2.add(rs.getString("i.rdate"));
                }
            
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    
    
