    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel d1=(DefaultTableModel)jTable1.getModel();
        int selectedIndex=jTable1.getSelectedRow();
        int id=Integer.parseInt(d1.getValueAt(selectedIndex,0).toString());
         
        String sid=sidt.getText();
        BookItem bitem=(BookItem) bc.getSelectedItem();
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String issuedate=date_format.format(idc.getDate());
        SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
        String returndate=date_format1.format(rdc.getDate());
        
        try {
            pst=con.prepareStatement("update issuebook set sid=?,bid=?,idate=?,rdate=? where id=?");
            pst.setString(1, sid);
            pst.setInt(2, bitem.id);
            pst.setString(3, issuedate);
            pst.setString(4, returndate);
            pst.setInt(5, id);
            int k= pst.executeUpdate();
            
            if(k==1)
            {
            
                JOptionPane.showMessageDialog(this, "Issue Detail Updated");
                sidt.setText("");
                bc.setSelectedIndex(-1);
                snt.setText("");
                sidt.requestFocus();
                IssueBook_Load();
                jButton1.setEnabled(true);
                
            }
            else{
            
                JOptionPane.showMessageDialog(this, "ERROR");
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

