    private void lb1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lb1ActionPerformed
        // TODO add your handling code here:
        String usn=usn1.getText();
        String pwd=pwd1.getText();
        
        try {

                pst=con.prepareStatement("select * from login where username=? and password=?");
                pst.setString(1, usn);
                pst.setString(2,pwd);
                rs=pst.executeQuery();
                if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Logged in Successfully");
                Main m= new Main();
                this.hide();
                m.setVisible(true);
            }
                else
            {
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password");
                 usn1.setText("");
                 pwd1.setText("");
                 usn1.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }//GEN-LAST:event_lb1ActionPerformed

