    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String sid=sidt.getText();
        String sname=snl.getText();
        String bname=bnl.getText();
        String rdate=rdl.getText();
        String elap=det.getText();
        String fine=ft.getText();
        
        
        
        try {
            pst=con.prepareStatement("insert into returnbook(sid,sname,bname,rdate,elp,fine)values(?,?,?,?,?,?)");
            pst.setString(1, sid);
            pst.setString(2, sname);
            pst.setString(3, bname);
            pst.setString(4, rdate);
            pst.setString(5, elap);
            pst.setString(6, fine);
            int k= pst.executeUpdate();
            
            pst=con.prepareStatement("delete from issuebook where sid=?");
            pst.setString(1, sid);
            pst.executeUpdate();
            
            if(k==1)
            {
            
                JOptionPane.showMessageDialog(this, "Book Returned");
                sidt.setText("");
                snl.setText("");
                bnl.setText("");
                sidt.setText("");
                rdl.setText("");
                ft.setText("");
                det.setText("");
                
                
                sidt.requestFocus();
                ReturnBook_Load();
                
                
            }
            else{
            
                JOptionPane.showMessageDialog(this, "ERROR");
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

