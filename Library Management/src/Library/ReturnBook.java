    private void sidtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sidtKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
        
            String sid;
            sid=sidt.getText();
            try {
                pst=con.prepareStatement("select s.studname,b.bname,i.rdate,datediff(now(),i.rdate)as elap from issuebook i JOIN student s ON i.sid=s.id JOIN book b ON i.bid=b.id and i.sid=?");
                pst.setString(1, sid);
                rs=pst.executeQuery();
                
                if(rs.next()==false)
                {
                    JOptionPane.showMessageDialog(this, "Student ID not found"); 
                }
                else{
                    String studentname=rs.getString("s.studname");
                    String bookname=rs.getString("b.bname");
                    snl.setText(studentname.trim());
                    bnl.setText(bookname.trim());
                    
                    String date=rs.getString("i.rdate");
                    rdl.setText(date);
                    
                    String elap=rs.getString("elap");
                    int elapsed=Integer.parseInt(elap);
                    if(elapsed>0)
                    {
                        det.setText(elap);
                        int fine=elapsed*10;
                        ft.setText(String.valueOf(fine));
                    }
                    else{
                    det.setText("0");
                    ft.setText("0");
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
    }//GEN-LAST:event_sidtKeyPressed

